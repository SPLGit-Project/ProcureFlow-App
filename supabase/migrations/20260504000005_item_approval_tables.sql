-- ─── item_approval_instances ──────────────────────────────────────────────────
-- One record per approval task generated by the approval engine

CREATE TABLE IF NOT EXISTS item_approval_instances (
  id                    UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  request_id            UUID NOT NULL REFERENCES item_requests(id) ON DELETE CASCADE,
  rule_id               UUID REFERENCES item_approval_rules(id),
  rule_name             TEXT NOT NULL,  -- Snapshot of rule name at time of creation

  -- Who needs to approve
  approver_type         TEXT NOT NULL DEFAULT 'ROLE',  -- 'ROLE' or 'USER'
  approver_role         TEXT,    -- Role name (e.g. 'MASTER_DATA_MANAGER')
  approver_user_id      UUID REFERENCES auth.users(id),  -- Set for USER type

  -- Sequential stage ordering
  stage_order           INTEGER NOT NULL DEFAULT 1,

  -- SLA
  sla_hours             INTEGER NOT NULL DEFAULT 72,
  sla_deadline          TIMESTAMPTZ,  -- Calculated on insert: created_at + sla_hours
  sla_breached          BOOLEAN NOT NULL DEFAULT false,

  -- Status
  status                approval_instance_status NOT NULL DEFAULT 'PENDING',

  -- Link to the decision (set when status transitions to APPROVED/REJECTED)
  decision_id           UUID,  -- FK to item_approval_decisions added below

  created_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at            TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- Auto-set SLA deadline on insert
CREATE OR REPLACE FUNCTION set_approval_sla_deadline()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  NEW.sla_deadline := NEW.created_at + (NEW.sla_hours || ' hours')::interval;
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_iai_sla_deadline
  BEFORE INSERT ON item_approval_instances
  FOR EACH ROW EXECUTE FUNCTION set_approval_sla_deadline();

-- updated_at trigger
CREATE OR REPLACE FUNCTION update_iai_updated_at()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN NEW.updated_at = now(); RETURN NEW; END;
$$;

CREATE TRIGGER trg_iai_updated_at
  BEFORE UPDATE ON item_approval_instances
  FOR EACH ROW EXECUTE FUNCTION update_iai_updated_at();

-- Auto-mark SLA as breached (checked by application scheduler; also set on SELECT)
CREATE OR REPLACE FUNCTION mark_sla_breached()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.sla_deadline IS NOT NULL AND NOW() > NEW.sla_deadline
     AND NEW.status = 'PENDING' AND NEW.sla_breached = false THEN
    NEW.sla_breached := true;
  END IF;
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_iai_sla_breach
  BEFORE UPDATE ON item_approval_instances
  FOR EACH ROW EXECUTE FUNCTION mark_sla_breached();

