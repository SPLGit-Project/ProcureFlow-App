-- ─── item_approval_decisions ─────────────────────────────────────────────────
-- Immutable audit log of every approval action

CREATE TABLE IF NOT EXISTS item_approval_decisions (
  id                UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  instance_id       UUID NOT NULL REFERENCES item_approval_instances(id),
  request_id        UUID NOT NULL REFERENCES item_requests(id),

  decision          approval_decision_type NOT NULL,
  decided_by        UUID NOT NULL REFERENCES auth.users(id),
  decided_at        TIMESTAMPTZ NOT NULL DEFAULT now(),

  -- MANDATORY comment — minimum 10 characters
  comments          TEXT NOT NULL CHECK (LENGTH(TRIM(comments)) >= 10),

  -- Escalation/delegation target
  escalated_to_user UUID REFERENCES auth.users(id),
  escalation_reason TEXT,

  -- Context snapshot at time of decision
  request_status_at_decision  item_request_status NOT NULL,
  item_summary                JSONB  -- Snapshot of key item fields for audit completeness
);

-- Immutability: decisions are forever
CREATE OR REPLACE FUNCTION enforce_iad_immutability()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  RAISE EXCEPTION 'Approval decisions are immutable and cannot be modified or deleted.';
END;
$$;

CREATE TRIGGER trg_iad_no_update
  BEFORE UPDATE ON item_approval_decisions
  FOR EACH ROW EXECUTE FUNCTION enforce_iad_immutability();

CREATE TRIGGER trg_iad_no_delete
  BEFORE DELETE ON item_approval_decisions
  FOR EACH ROW EXECUTE FUNCTION enforce_iad_immutability();

-- When a decision is recorded, update the linked approval instance
CREATE OR REPLACE FUNCTION update_instance_on_decision()
RETURNS TRIGGER LANGUAGE plpgsql SECURITY DEFINER AS $$
DECLARE
  new_status approval_instance_status;
BEGIN
  -- Map decision type to instance status.
  -- DELEGATED is not a valid approval_instance_status — treat as ESCALATED
  -- because the intent (routing to another party) is the same.
  new_status := CASE NEW.decision
    WHEN 'APPROVED'   THEN 'APPROVED'::approval_instance_status
    WHEN 'REJECTED'   THEN 'REJECTED'::approval_instance_status
    WHEN 'ESCALATED'  THEN 'ESCALATED'::approval_instance_status
    WHEN 'DELEGATED'  THEN 'ESCALATED'::approval_instance_status  -- DELEGATED has no matching instance status
    ELSE 'PENDING'::approval_instance_status
  END;

  UPDATE item_approval_instances
  SET status = new_status,
      decision_id = NEW.id,
      updated_at = now()
  WHERE id = NEW.instance_id;
  RETURN NEW;
END;
$$;

CREATE TRIGGER trg_iad_update_instance
  AFTER INSERT ON item_approval_decisions
  FOR EACH ROW EXECUTE FUNCTION update_instance_on_decision();

